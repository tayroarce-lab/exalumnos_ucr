CREATE INDEX IF NOT EXISTS idx_users_directorio ON public.users (id) WHERE visible_en_directorio = true AND deleted_at IS NULL;
CREATE INDEX IF NOT EXISTS idx_users_carrera_principal ON public.users (carrera_principal_id);
