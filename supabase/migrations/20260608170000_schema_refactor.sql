CREATE OR REPLACE FUNCTION public.is_admin() RETURNS boolean AS $$
BEGIN
  RETURN (current_setting('request.jwt.claims', true)::jsonb ->> 'user_role') = 'admin';
END;
$$ LANGUAGE plpgsql STABLE;

-- Actualizar políticas básicas
DROP POLICY IF EXISTS "users_select_authenticated" ON public.users;
CREATE POLICY "Directorio publico" ON public.users FOR SELECT 
USING (visible_en_directorio = true AND deleted_at IS NULL);

DROP POLICY IF EXISTS "users_update_own" ON public.users;
CREATE POLICY "Modificar propio perfil" ON public.users FOR UPDATE 
USING (auth.uid() = id) 
WITH CHECK (auth.uid() = id);

